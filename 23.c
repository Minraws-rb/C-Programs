#include <stdio.h>
#include <math.h>
int main() {
    float n1 = 4.5, n2 = -7.2;

    printf("Number: %.2f\n", n1);
    printf("Floor value: %.2f\n", floor(n1));
    printf("Ceil value: %.2f\n", ceil(n1));
    printf("\nNumber: %.2f\n", n2);
    printf("Floor value: %.2f\n", floor(n2));
    printf("Ceil value: %.2f\n", ceil(n2));
    return 0;
}
